# Ternary Operator

if a > b
  result = "A is greater"
else
  result = "B is greater"
end



result = a > b ? "A is greater": "B is greater"



result = logged_in? && admin? ? "Admin logged In": "Admin is not logged In"



if logged_in? && admin?
  result = "Admin logged In"
else
  result = "Admin is not logged In"
end
